package fr.uga.pddl4j.parser;

/**
 * The enumeration used to specified the different types of the symbol.
 */
public enum SymbolType {
    /**
     * The predicate symbol.
     */
    PREDICATE,
    /**
     * The type symbol.
     */
    TYPE,
    /**
     * The action symbol.
     */
    ACTION,
    /**
     * The method symbol.
     */
    METHOD,
    /**
     * The task symbol.
     */
    TASK,
    /**
     * The preference symbol.
     */
    PREFERENCE,
    /**
     * The functor symbol.
     */
    FUNCTOR,
    /**
     * The variable symbol.
     */
    VARIABLE,
    /**
     * The duration variable symbol.
     */
    DURATION_VARIABLE,
    /**
     * The continuous variable symbol.
     */
    CONTINUOUS_VARIABLE,
    /**
     * The constant symbol.
     */
    CONSTANT,
    /**
     * The domain symbol.
     */
    DOMAIN,
    /**
     * The problem symbol.
     */
    PROBLEM,
    /**
     * the task id symbol.
     */
    TASK_ID,
}
